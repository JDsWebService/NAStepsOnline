	<p>In considering the spiritual principles intrinsic to Step Three, we will focus first on surrender and willingness. Then we will look at how hope translates into faith and trust. Finally, we will see how the principle of commitment is fled to the Third Step.</p>

	<p>Practicing the principle of surrender is easy for us when everything is going along as we'd like-we think. Actually, when things are going smoothly, it's more likely that we are being lulled into a belief that we're in charge, which doesn't require much "surrender." Keeping the principle of surrender to the care of the God of our understanding alive in our spirits is essential, even when things are going well.</p>
	
	<ul>
	<li>What am I doing to reinforce my decision to allow my Higher Power to care for my will and my life?</li>
	<li>How does the Third Step allow me to build on the surrender I've developed in Steps One and Two?</li>
	</ul>

	<p>We usually feel most willing immediately following a surrender. Willingness often comes in the wake of despair or a struggle for control. We can practice the principle of willingness, though, before it becomes necessary and possibly save ourselves some pain.</p>

	<ul>
	<li>In what ways have I demonstrated willingness in my recovery so far?</li>
	<li>Am I fighting anything in my recovery? What do I think would happen if I became willing to let</li>
	recovery prevail in that area of my life?
	</ul>

	<p>There is a spiritual progression from hope to faith to trust in the Third Step. As we begin Step Three, we carry with us the sense of hope that was born in us as we worked the Second Step. Hope springs from the knowledge that our life is full of possibilities - there are no hard certainties yet, just the first whispers of anticipation that we just may be able to fulfill our heart's deepest desires. Lingering doubts fade as hope becomes faith. Faith propels us forward into action; we actually do the work that those we have faith in are telling us is necessary if we are to achieve what we want. In the Third Step, faith gives us the capacity to actually make a decision and carry that decision into action. Trust comes into play after faith has been applied. We have probably made significant progress toward fulfilling our goals; now we have evidence that we can influence the course of our lives through taking positive action.</p>
	
	<ul>
	<li>How have hope, faith, and trust become positive forces in my life?</li>
	<li>What further action can I take to apply the principles of hope, faith, and trust in my recovery?</li>
	<li>What evidence do I have that I can trust confidently in my recovery?</li>
	</ul>

	<p>The principle of commitment is the culmination of the spiritual process of Step Three. Making the decision to "turn it over," over and over again, even when our decision doesn't seem to be having any positive effect, is what this step is all about. We can practice the spiritual principle of commitment by reaffirming our decision on a regular basis and by continuing to take action that gives our decision substance and meaning-for instance, working the rest of the steps.</p>
	
	<ul>
	<li>What have I done recently that demonstrates my commitment to recovery and to working a program? (For example: Have I taken a service position in NA? Have I agreed to sponsor another recovering addict? Have I continued to go to meetings no matter what I was feeling about them? Have I continued to work with my sponsor even after he or she told me an unpleasant truth or gave me some direction I didn't want to follow? Did I follow that direction?)</li>
	</ul>

