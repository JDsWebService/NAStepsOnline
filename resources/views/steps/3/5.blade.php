	<p>The order in which we prepare to surrender our will and our lives to the care of the God of our understanding is significant. Many of us have found that we actually follow the order in the step: First, we turn over our will; then, gradually, we turn over our lives. It seems that it's easier for us to grasp the destructive nature of our self-will and see that it must be surrendered; consequently, it's usually the first to go. Harder for us to grasp is the need to turn over our lives and the process of that surrender. For us to be comfortable with allowing our Higher Power to care for our lives, we will have to develop some trust. We may have no trouble turning over our addiction, but want to remain in control of the rest of our lives. We may trust our Higher Power to care for our work lives, but not our relationships. We may trust our Higher Power to care for our partners1 but not our children. We may trust our Higher Power with our safety, but not our finances. Many of us have trouble letting go completely. We think we trust our Higher Power with certain areas of our lives, but immediately take back control the first time we get scared or things aren't going the way we think they should. It's necessary for us to examine our progress in turning it over.</p>
	
	<ul>
		<li>What does "to the care of" mean to me?</li>
		<li>What does it mean for me to turn my will and my life over to the care of the God of my understanding?</li>
		<li>How might my life be changed if I make the decision to turn it over to my Higher Power's care?</li>
		<li>How do I allow my Higher Power to work in my life?</li>
		<li>How does my Higher Power care for my will and my life?</li>
		<li>Have there been times when I have been unable to let go and trust God to care for the outcome of a particular situation? Describe.</li>
		<li>Have there been times when I have been able to let go and trust God for the outcome? Describe.</li>
	</ul>

	<p>To turn our will and our lives over to the care of our Higher Power, we must take some kind of action. Many of us find that it works best for us to make some formal declaration on a regular basis. We may want to use the following quote from our Basic Text: "Take my will and my life. Guide me in my recovery. Show me how to live." This seems to capture the essence of Step Three for many of us. However, we can certainly feel free to find our own words, or to find a more informal way of taking action. Many of us believe that every day we abstain from using, or take suggestions from our sponsor, we are taking practical action on our decision to turn our will and lives over to the care of our Higher Power.</p>

