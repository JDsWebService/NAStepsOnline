	<p>Step Three is critical because we've acted on self-will for so long, abusing our right to make choices and decisions. So what exactly is self-will? Sometimes it's total withdrawal and isolation. We end up living a very lonely and self-absorbed existence. Sometimes self-will causes us to act to the exclusion of any considerations other than what we want. We ignore the needs and feelings of others. We barrel through, stampeding over anyone who questions our right to do whatever we want. We become tornadoes, whipping through the lives of family, friends, and even strangers, totally unconscious of the path of destruction we have left behind. If circumstances aren't to our liking, we try to change them by any means necessary to achieve our aims. We try to get our way at all costs. We are so busy aggressively pursuing our impulses that we completely lose touch with our conscience and with a Higher Power. To work this step, each one of us needs to identify the ways in which we have acted on self-will.</p>
	
	<ul>
		<li>How have I acted on self-will? What were my motives?</li>
		<li>How has acting on self-will affected my life? How has my self-will affected others?</li>
	</ul>

	<p>Surrendering our self-will doesn't mean we can't pursue goals or try to make changes in our lives and the world. It doesn't mean we have to passively accept injustices to ourselves or to people for whom we're responsible. We need to differentiate between destructive self-will and constructive action.</p>
	
	<ul>
		<li>Will pursuing my goals harm anyone? How?</li>
		<li>In the pursuit of what I want, is it likely that I will end up doing something that adversely affects myself or others? Explain.</li>
		<li>Will I have to compromise any of my principles to achieve this goal? (For example: Will I have to be dishonest? Cruel? Disloyal?)</li>
	</ul>

	<p>If we are new in the program and just beginning to work Step Three, we will probably end up wondering what God's will is for us, thinking that the step asks us to find this out. Actually, we don't formally focus our attention on seeking knowledge of our Higher Power's will for us until the Eleventh Step, but we do begin the process that will lead us to that point in Step Three.</p>

	<p>God's will for us is something we will gradually come to know as we work the steps. At this point we can come to some very simple conclusions about our Higher Power's will for us that will serve us well for the time being. It is our Higher Power's will for us to stay clean. It is our Higher Power's will for us to do things that will help us stay clean, such as going to meetings and talking to our sponsor regularly.</p>
	
	<ul>
		<li>Describe the times when my will hasn't been enough. (For example, I couldn't stay clean on my own will.)</li>
		<li>What is the difference between my will and God's will?</li>
	</ul>

	<p>At some point in our recovery, we may find that we have somehow shifted from trying to align our will with a Higher Power's to running on self-will. This happens so slowly and subtly that we hardly even notice. It seems as though we're especially vulnerable to self-will when things are going well. We cross the fine line that divides humble and honest pursuit of goals from subtle manipulation and forced results. We find ourselves going just a little too far in a discussion to convince someone that we are right. We find ourselves holding on to something just a little too long. We suddenly realize that we haven't contacted our sponsor in quite a while. We feel a quiet, almost subconscious discomfort that will alert us to this subtle shift away from recovery - if we listen.</p>
	
	<ul>
		<li>Have there been times in my recovery when I've found myself subtly taking back my will and my life? What alerted me? What have I done to recommit myself to the Third Step?</li>
	</ul>

