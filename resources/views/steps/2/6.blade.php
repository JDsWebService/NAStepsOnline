	<p>It Works: How and Why defines the term "restoration" as "changing to a point where addiction and its accompanying insanity are not controlling our lives."</p>

	<p>We find that just as our insanity was evident in our loss of perspective and sense of proportion, so we can see sanity in our lives when we begin developing a perspective that allows us to make better decisions. We find that we have choices about how to act. We begin to have the maturity and wisdom to slow down and consider all aspects of a situation before acting.</p>

	<p>Naturally, our lives will change. Most of us have no trouble identifying the sanity in our lives when we compare our using with our early recovery, our early recovery with some time clean, and some time clean with long-term recovery. All of this is a process, and our need for a restoration to sanity will change over time.</p>

	<p>When we're new in the program, being restored to sanity probably means not having to use anymore; when that happens, perhaps some of the insanity that is directly and obviously tied to our using will stop. We'll quit committing crimes to get drugs. We'll cease putting ourselves in certain degrading situations that serve no purpose but our using.</p>

	<p>If we've been in recovery for some time, we may find that we have no trouble believing in a Power greater than ourselves that can help us stay clean, but we may not have considered what a restoration to sanity means to us beyond staying clean. As we grow in our recovery, it's very important that our idea of the meaning of "sanity" also grows.</p>
	
	<ul>
		<li>What are some things I consider examples of sanity?</li>
		<li>What changes in my thinking and behavior are necessary for my restoration to sanity? In what areas of my life do I need sanity now?</li>
		<li>How is restoration a process?</li>
		<li>How will working the rest of the steps help me in my restoration to sanity?</li>
		<li>How has sanity already been restored to me in my recovery?</li>
	</ul>

	<p>Some of us may have unrealistic expectations about being restored to sanity. We may think that we'll never get angry again or that, as soon as we start to work this step, we will behave perfectly all the time and have no more trouble with obsessions, emotional turmoil, or imbalance in our lives. This description may seem extreme, but if we find ourselves disappointed with our personal growth in recovery or the amount of time it takes to be "restored to sanity," we may recognize some of our beliefs in this description. Most of us have found that we gain the most serenity by letting go of any expectations we may have about how our recovery is progressing.</p>

	<ul>
		<li>What expectations do I have about being restored to sanity? Are they realistic, or unrealistic?</li>
		<li>Are my realistic expectations about how my recovery is progressing being met or not? Do I understand that recovery happens over time, not overnight?</li>
		<li>Finding ourselves able to act sanely, even once, in a situation with which we were never able to deal successfully before is evidence of sanity.</li>
		<li>Have I had any experiences like that in my recovery? What were they?</li>
	</ul>

