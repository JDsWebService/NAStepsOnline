	<p>Each one us comes to recovery with a whole history of life experiences. That history will determine to a large degree the kind of understanding we develop of a Power greater than ourselves. In this step, we don't have to have a lot of specific ideas about the nature or identity of that Higher Power. That sort of understanding will come later. The kind of understanding of a Higher Power that's most important to find in the Second Step is an understanding that can help us. We're not concerned here with theological elegance or doctrinal adherence - we just want something that works.</p>

	<p>How powerful does a Power greater than ourselves have to be? The answer to that question is simple. Our addiction as a negative power was, without a doubt, greater than we were. Our addiction led us down a path of insanity and caused us to act differently than we wanted to behave. We need something to combat that, something at least as powerful as our addiction.</p>
	
	<ul>
		<li>Do I have problems accepting that there is a power or powers greater than myself?</li>
		<li>What are some things that are more powerful than I am?</li>
		<li>Can a Power greater than myself help me stay clean? How?</li>
		<li>Can a Power greater than I am help me recover? How?</li>
	</ul>

	<p>Some of us may have a very clear idea about the nature of a Power greater than ourselves, and there's absolutely nothing wrong with that. In fact, Step Two is the point at which many of us begin to form our first practical ideas about a Power greater than ourselves, if we haven't already. Many addicts have found it helpful to identify what a Power greater than ourselves is not before identifying what it is. In addition, looking at what a Power greater than ourselves can do for us may help us begin to discover more about that Power.</p>

	<p>There are many, many understandings of a Power greater than ourselves that we can develop. We can think of it as the power of spiritual principles, the power of the NA Fellowship, "Good Orderly Direction," or anything else of which we can conceive, as long as it is loving and caring and more powerful than we are. As a matter of fact, we don't have to have any understanding at all of a Power greater than ourselves to be able to use that Power to stay clean and seek recovery.</p>
	
