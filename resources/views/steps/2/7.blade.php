	<p>In the Second Step, we will focus on open-mindedness, willingness, faith, trust, and humility. The principle of open-mindedness that we find in the Second Step arises from the understanding that we can't recover alone, that we need some kind of help. It continues with opening our minds to believing that help is possible for us. It doesn't matter whether we have any idea of how this Power greater than ourselves is going to help, just that we believe it's possible.</p>
	
	<ul>
		<li>Why is having a closed mind harmful to my recovery?</li>
		<li>How am I demonstrating open-mindedness in my life today?</li>
		<li>In what ways has my life changed since I've been in recovery? Do I believe more change is possible?</li>
	</ul>

	<p>Practicing the principle of willingness in the Second Step may begin simply. At first we may just go to meetings and listen to other recovering addicts share about their experiences with this step. Then we may begin applying what we hear to our own recovery. Of course, we ask our sponsor to guide us.</p>
	
	<ul>
		<li>What am I willing to do to be restored to sanity?</li>
		<li>Is there something I am now willing to do that I was previously unwilling to do? What is it?</li>
	</ul>

	<p>We can't just sit back and wait to feel a sense of faith when working Step Two. We have to work at it. One of the suggestions that has worked for many of us is to "act as if" we had faith. This doesn't mean that we should be dishonest with ourselves. We don't need to lie to our sponsor or anyone else about where we are with this step. We're not doing this to sound good or look good. "Acting as if" simply means living as though we believe that what we hope for will happen. In the Second Step, this would mean living as though we expect to be restored to sanity. There are a variety of ways this may work in our individual lives. Many members suggest that we can begin "acting as if" by going to meetings regularly and taking direction from our sponsor.</p>
	
	<ul>
		<li>What action have I been taking that demonstrates my faith? How has my faith grown?</li>
		<li>Have I been able to make plans, having faith that my addiction isn't going to get in the way?</li>
	</ul>

	<p>Practicing the principle of trust may require overcoming a sense of fear about the process of being restored to sanity. Even if we've been clean only a short time, we've probably already experienced some emotional pain as we've grown in recovery. We may be afraid that there will be more pain. In one sense, we're right about this: There will be more pain. None of it, however, will be more than we can bear, and none of it has to be borne alone. If we can develop our sense of trust in the process of recovery and in a Power greater than ourselves, we can walk through the painful times in our recovery. We'll know that what's waiting on the other side will be more than just superficial happiness, but a fundamental transformation that will make our lives more satisfying on a deeper level.</p>
	
	<ul>
		<li>What fears do I have that are getting in the way of my trust?</li>
		<li>What do I need to do to let go of these fears?</li>
		<li>What action am I taking that demonstrates my trust in the process of recovery and a Power greater than myself?</li>
	</ul>

	<p>The principle of humility springs from our acknowledgment that there is a Power greater than ourselves. It's a tremendous struggle for most of us to stop relying on our own thinking and begin to ask for help, but when we do, we have begun to practice the principle of humility found in the Second Step.</p>
	
	<ul>
		<li>Have I sought help from a Power greater than myself today? How?</li>
		<li>Have I sought help from my sponsor, gone to meetings, and reached out to other recovering addicts? What were the results?</li>
	</ul>

