	<p>In the First Step, we will focus on honesty, open-mindedness, willingness, humility, and acceptance. The practice of the principle of honesty from the First Step starts with admitting the truth about our addiction, and continues with the practice of honesty on a daily basis. When we say "I'm an addict" in a meeting, it may be the first truly honest thing we've said in a long time. We begin to be able to be honest with ourselves and, consequently, with other people.</p>
	
	<ul>
	<li>If I've been thinking about using or acting out on my addiction in some other way, have I shared it with my sponsor or told anyone else?</li>
	<li>Have I stayed in touch with the reality of my disease, no matter how long I've had freedom from active addiction?</li>
	<li>Have I noticed that, now that I don't have to cover up my addiction, I no longer need to lie like I did? Do I appreciate the freedom that goes along with that? In what ways have I begun to be honest in my recovery?</li>
	</ul>

	<p>Practicing the principle of open-mindedness found in Step One mostly involves being ready to believe that there might be another way to live and being willing to try that way. It doesn't matter that we can't see every detail of what that way might be, or that it may be totally unlike anything we've heard about before; what matters is that we don't limit ourselves or our thinking. Sometimes we may hear NA members saying things that sound totally crazy to us, things like "surrender to win" or suggestions to pray for someone we resent. We demonstrate open-mindedness when we don't reject these things without having tried them.</p>
	
	<ul>
	<li>What have I heard in recovery that I have trouble believing? Have I asked my sponsor or the person I heard say it, to explain it to me?</li>
	<li>In what ways am I practicing open-mindedness?</li>
	</ul>

	<p>The principle of willingness contained in the First Step can be practiced in a variety of ways. When we first begin to think about recovery, many of us either don't really believe it's possible for us or just don't understand how it will work, but we go ahead with the First Step anyway-and that's our first experience with willingness. Taking any action that will help our recovery shows willingness: going to meetings early and staying late, helping set up meetings, getting other NA members' phone numbers and calling them.</p>
	
	<ul>
	<li>Am I willing to follow my sponsor's direction?</li>
	<li>Am I willing to go to meetings regularly?</li>
	<li>Am I willing to give recovery my best effort? In what ways?</li>
	</ul>

	<p>The principle of humility, so central to the First Step, is expressed most purely in our surrender. Humility is most easily identified as an acceptance of who we truly are - neither worse nor better than we believed we were when we were using, just human.</p>
	
	<ul>
	<li>Do I believe that I'm a monster who has poisoned the whole world with my addiction? Do I believe that my addiction is utterly inconsequential to the larger society around me? Or something in between?</li>
	<li>Do I have a sense of my relative importance within my circle of family and friends? In society as whole? What is that sense?</li>
	<li>How am I practicing the principle of humility in connection with this work on the First Step?</li>
	</ul>

	<p>To practice the principle of acceptance, we must do more than merely admit that we're addicts. When we accept our addiction, we feel a profound inner change that is underscored by a rising sense of hope. We also begin to feel a sense of peace. We come to terms with our addiction, with our recovery, and with the meaning those two realities will come to have in our lives. We don't dread a future of meeting attendance, sponsor contact, and step work; instead, we begin to see recovery as a precious gift, and the work connected with it as no more trouble than other routines of life.</p>
	
	<ul>
	<li>Have I made peace with the fact that I'm an addict?</li>
	<li>Have I made peace with the things I'll have to do to stay clean?</li>
	<li>How is acceptance of my disease necessary for my continued recovery?</li>
	</ul>

